// 사용자 정의 데이터 타입 - 메서드 = 연산자

package step07;

public class Score2 {
    String name;
    int kor;
    int eng;
    int math;
    int sum;
    float average;
    
    // 메서드를 이용하여 이 타입의 데이터를 다룰 수 있는 연산자를 정의한다.
    // => 사용자 정의 데이터 타입 입장에서는 메서드가 연산자 역할을 한다.
    // => 즉, 사용자 정의 데이터 타입에 메서드를 정의하는 것은
    //    그 데이터를 다룰 연산자를 정의하는 것이다.
    
    // ----부가설명----------------------------------------------
    // 항상 어떤 데이터 타입이 있으면 그 데이터에 대해서 다룰 수 있는 연산자가 이미 만들어져 있다.(*,+,-,/,>>,<<,>>>,&,|...)
    // 그러나개발자가 새로운 형태의 데이터 타입을 만들었으면 그 메모리에 담겨져있는 데이터를 다룰수있는 연산자는 없다.
    // 따라서 그 새로운 형태의 데이터를 다룰 수 있는 연산자가 필요하다.
    // => 그 연산자는 메서드라는 문법을 사용해서 만든다.
   
    // 사용자 정의 데이터 타입이라는 관점에서 메소드를 바라보면 메소드는 연산자 역할을 한다.
    // 어느 방향에서 보느냐에 따라서 메소드의 역할이 다르다.
    // --------------------------------------------------------
    
    // Score2 데이터 값을 다룰 수 있는 새 연산자를 정의
    // => 다음 메서드는 Score 객체의 국,영,수 값의 합계와 평군을 계산하는 연산자이다.
    public static void calculator(Score2 score) { // Score2라는 메모리 주소를 준다.
        score.sum = score.kor + score.eng + score.math;
        score.average = score.sum / 3f;
    }
    
    
    
    
    
    
    
    
}

// 산술 연산자 : +, -, *, /, %
package step04;

public class Exam01_1 {
    public static void main(String[] args) {
        System.out.println(100 + 27);
        System.out.println(100 - 27);
        System.out.println(100 * 27);
        System.out.println(100 / 27);
        System.out.println(100 % 27); // 나눈 나머지 값
    }
}